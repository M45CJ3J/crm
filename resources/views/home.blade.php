    new Chart(productCtx, {
        type: 'pie',
        data: {
            labels: productData.labels,
            datasets: [{
                label: 'Product Quantity',
                data: productData.data,
                backgroundColor: [
                    'rgba(255, 99, 132, 0.2)',
                    'rgba(54, 162, 235, 0.2)',
